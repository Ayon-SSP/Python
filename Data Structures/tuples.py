# Declairing the tuple a data structure which is immutable
# Here we can save all primitive data types

my_tuples = ("ram", True, 12, 0, 12)
# Printing the tuple 
print(my_tuples)

# To print the length of tuples
print(len(my_tuples)) # 4, because 12 has only taken as 1

# To copy the tuples
new_tuples = my_tuples
# Printing the updated tuples
print(new_tuples)
